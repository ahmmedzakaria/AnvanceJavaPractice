    private void bSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bSaveActionPerformed
        String name = tfName.getText().toString();
        String address = tfAddress.getText().toString();
        String roll = tfRoll.getText().toString();
        String sclass = tfClass.getText().toString();
        if (StudentControler.saveEmployee(name, address, roll, sclass)) {
            clearFilds();
            JOptionPane.showMessageDialog(this, "Student Information Saved", "Success", JOptionPane.PLAIN_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Insertion Failed", "Error!", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_bSaveActionPerformed

