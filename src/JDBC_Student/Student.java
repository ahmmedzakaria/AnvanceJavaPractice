    private void bUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bUpdateActionPerformed
        String id=tfId.getText().toString();
        String name = tfName.getText().toString();
        String address = tfAddress.getText().toString();
        String roll = tfRoll.getText().toString();
        String sclass = tfClass.getText().toString();
        if (StudentControler.updateStudent(id,name, address, roll, sclass)) {
            clearFilds();
            JOptionPane.showMessageDialog(this, "Student Information Updated", "Success", JOptionPane.PLAIN_MESSAGE);
        } else {
            JOptionPane.showMessageDialog(this, "Not Updated", "Error!", JOptionPane.PLAIN_MESSAGE);
        }
    }//GEN-LAST:event_bUpdateActionPerformed

    private void bShowActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bShowActionPerformed
        String id=tfId.getText().toString();
        ArrayList list=StudentControler.showStudent(id);
        tfName.setText(list.get(0).toString());
        tfAddress.setText(list.get(1).toString());
        tfRoll.setText(list.get(2).toString());
        tfClass.setText(list.get(3).toString());
        
    }//GEN-LAST:event_bShowActionPerformed

